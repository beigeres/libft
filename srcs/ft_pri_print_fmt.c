#include "ft_printf.h"

void	ft_pri_print_fmt(char **str, const char *fmt, int i)
{
	while (fmt[i] != '\0' && fmt[i] != '%')
	{
		**str = fmt[i];
		i++;
		(*str)++;
	}
}
