#include "ft_printf.h"

int	ft_pri_size_wchar(wchar_t wc)
{
	if (wc < 0 || (wc >= 55296 && wc < 57344) ||
			(wc >= 1114112))
		return (-1);
	return (ft_wchar_dig(wc));
}
