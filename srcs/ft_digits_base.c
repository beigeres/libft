#include "libft.h"

int	ft_digits_base(int n, int b)
{
	int		d;

	d = 0;
	if (n == INT_MIN)
	{
		n /= b;
		d++;
	}
	if (n < 0)
	{
		d++;
		n = -n;
	}
	while (n >= b)
	{
		n /= b;
		d++;
	}
	return (d + 1);
}
